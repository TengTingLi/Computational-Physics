    public void calculate(int n_par, int n_step){
        double rnd_x, rnd_y;
        
        
        for(int n = 0; n < n_par; n ++){
            double x = 0, y = 0;
            for(int i = 0; i < n_step; i ++){
                rnd_x = Math.random();
                rnd_y = Math.random();
                x = rnd_x < 0.5 ? x - 1 : x + 1;
                y = rnd_y < 0.5 ? y - 1 : y + 1;
                
            }
            plot_point(x,y);
        }
    }
    
